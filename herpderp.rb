class Herpderp

  def one
    "Herp derpsum der derp ner le, dee tee? Derpy me sherpus berps le. Le merp sherper derpus dee ler. Herderder herpem perp terpus berp pee! Ler mer, me herp berp herderder terpus. Ler mer derpsum sherper le ner perper der. Derps ler tee merpus le me berps dee. Dee le berp terp merp! Herpler derpler perp derperker sherlamer ter ler terp. Perp derpler serp herderder herpsum, berp ter herp berps? Le perp ler derp serp derps merpus berp dee sherp. Sherp ner herderder dee berps serp ler pee! Mer derp merp herpy. Herp der herpderpsmer mer herpy terp derperker berps!"
  end

  def two
    "Herp derpsum tee ter, merp herpderpsmer sherper der. Derpy merp, me derperker perp ler. Mer terp nerpy berps, derpler ter herpderpsmer derps serp sherper. Herpderpsmer terp derp herpler dee me! Der terp le ter derpus ner berp merp ler sherp? Derpy herpy perp ler merp pee herpem derp ter herpderpsmer? Derps le ner, derpus herpler berp. Herpler dee nerpy perper derperker mer herpsum terpus merp herpderpsmer. Sherp derpy berp sherpus pee herpderpsmer."
  end

  def three
    "Derperker berp nerpy sherper, dee derpsum mer perper der! Berps ter herpy serp herpler perper merpus terp der? Dee perper me, merpus pee. Sherlamer terp pee merpus, sherp serp derp sherpus ter. Nerpy derp ler sherper sherlamer herpderpsmer. Le ner berp dee serp perp herderder merp, herpy ter! Terp herpy ler derp? Dee terp berps derperker sherp mer serp tee sherper ler? Derps serp derperker mer ner. Derp ner dee herpsum berp merpus herpderpsmer herpler!"
  end

  def four
    "Herpderpsmer le nerpy berp. Tee derpus perper dee derperker terpus pee berp nerpy! Derpus pee ler, der herderder herp. Pee me, perper herderder derpus herp derpy berps mer. Merp ner perp dee! Tee derp sherp merp terp? Terp merp le derpy derpler dee. Derperker sherpus, berp mer perp. Me merp sherpus derpus. Merp derpler, pee mer der dee. Derpus me berp herpsum le derpy."
  end

end
